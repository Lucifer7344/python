# there are 7 type of assignment operator
# '=' assignment operator is used to assign value
# 1- '+=' operator
a=10
b=20
a+=b
print(a)

# 2- '-=' operator

a=20
b=10
a-=b
print(a)

# 3- '*=' operator
a=20
b=5
a*=b
print(a)

# 4- '/=' operator
a=50
b=5
a/=b
print(a)

# 5- '%=' operator
a=10
b=3
a%=b
print(a)

# 6- '**='(power) operator
a=5
b=3
a**=b
print(a) 